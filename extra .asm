.MODEL SMALL
.STACK 100H
.DATA
VAR1 DB 'UPPERRR $'
VAR2 DB 'LOWERRR $'
VAR3 DB 'NOTHINGGG$'
.CODE
MAIN PROC
MOV AX,@DATA ;INITIALIZATION OF DS
MOV DS,AX

MOV AH,1
INT 21H
MOV BL,AL
CMP BL,'A'
JL PRINT3  


CMP BL,'Z'
JLE PRINT1
CMP BL,'z'
JLE PRINT2

JMP PRINT3
PRINT1: 


LEA DX,VAR1 
MOV AH,9 
INT 21H 
JMP EXIT 


PRINT2:
LEA DX,VAR2 
MOV AH,9 
INT 21H 
JMP EXIT

PRINT3:
LEA DX,VAR3 
MOV AH,9 
INT 21H 
JMP EXIT
EXIT: 


MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN